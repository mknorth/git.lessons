package lesson4.loops;

public class JavaArithmetical {
    public static void main(String[] args) {

            int a = 10;
            int b = 5;
            int c = 8;

            int result;

            result = a + b;
            System.out.println("a+b: "+result);

            result = a - b;
            System.out.println("a-b: "+result);

            result = a * b;
            System.out.println("a*b: "+result);

            result = a / b;
            System.out.println("a/b: "+result);


            result = a / c;
            System.out.println("a/c: "+result);

            result = a % c;
            System.out.println("a%c: "+result);

        }


    }

